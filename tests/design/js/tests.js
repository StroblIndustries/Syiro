/// <reference path="syiro.d.ts" />
/// <reference path="perfily.d.ts" />

function RunBenchmarks(){

	// #region Syiro Init Benchmark

	var syiroInitPerfilyInstance = new Perfily({ // Create a Perfily instance for initialization of Syiro
		"name" : "Syiro Init",
		"function" : function(){
			syiro.Init();
		}
	})

	syiroInitPerfilyInstance.Run(); // Run syiroInitPerfilyInstance

	// #endregion

	// #region Syiro ElementCreator Benchmark

	var generatedSyiroElement; // Define generatedSyiroElement as the element generated by the ElementCreator

	var syiroElementCreatorPerfilyInstance = new Perfily({ // Create a Perfily instance for ElementCreator in Syiro
		"name" : "Syiro ElementCreator",
		"function" : function(){
			generatedSyiroElement = syiro.utilities.ElementCreator("p", { "content" : "Example paragraph" });
		}
	});

	syiroElementCreatorPerfilyInstance.Run(); // Run syiroElementCreatorPerfilyInstance

	// #endregion

	// #region Syiro Remove() Benchmark

	var timeValidObject = syiro.utilities.SecondsToTimeFormat(4523); // Create a "valid" time Object to use with the Perfily instance

	var syiroSecondsToTimeFormatPerfilyInstance = new Perfily({ // Create a Perfily instance for Syiro's SecondsToTimeFormat
		"name" : "Syiro SecondsToTimeFormat",
		"expecting" : JSON.stringify(timeValidObject), // Compare the JSON of timeValidObject with that provided by SecondsToTimeFormat
		"function" : function(){
			var localTimeObject = syiro.utilities.SecondsToTimeFormat(4523);
			return JSON.stringify(localTimeObject);
		}
	})

	syiroSecondsToTimeFormatPerfilyInstance.Run(); // Run syiroRemovePerfilyInstance

	// #endregion

	// #region Syiro Add() Benchmark

	var syiroAddPerfilyInstance = new Perfily({ // Create a Perfily instance for adding a component or Element via Syiro
		"name" : "Syiro Add",
		"function" : function(){
			syiro.component.Add("append", document.body, generatedSyiroElement);
		}
	})

	syiroAddPerfilyInstance.Run(); // Run syiroAddPerfilyInstance

	// #endregion

	// #region Syiro Remove() Benchmark

	var syiroRemovePerfilyInstance = new Perfily({ // Create a Perfily instance for removing a component or Element via Syiro
		"name" : "Syiro Remove",
		"function" : function(){
			syiro.component.Remove(generatedSyiroElement);
		}
	})

	syiroRemovePerfilyInstance.Run(); // Run syiroRemovePerfilyInstance

	// #endregion
}